package item;

public class Additem extends Super {
	
	private int id  ;
	private String vehicle  ;
	private String number ;
	private String year ;
	
	
	public Additem(int id, String vehicle, String number, String year) {
		super();
		this.id = id;
		this.vehicle = vehicle;
		this.number = number;
		this.year = year;
	}

	
	

	public String getVehicle() {
		return vehicle;
	}








	public String getNumber() {
		return number;
	}




	




	public String getYear() {
		return year;
	}




	









	




	@Override
	public int getId() {
		return id;
	}
	
	
	


}
